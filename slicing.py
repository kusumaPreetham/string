#string slicing
name = "preetham"
print(name[0:3])
print(name[:])

#negative slicing
print(name[-8:-5])
print(name[:])

#reverse string
name ="preetham"
print(name[::-3])
print(name[::-2])

#revese list
name = ["preetham","rajeev","ahmed","sai"]
print(name[::-1])

#reverse a list without a list
name = ["preetham","rajeev","ahmed","sai"]
name.reverse()
print(name)


